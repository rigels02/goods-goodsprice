    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        if (jTxtStartDate.getText().isEmpty()
                || jTxtEndDate.getText().isEmpty()) {
            return;
        }
        DateLabelFormatter formater = new DateLabelFormatter();

        Date startDate = null;
        Date endDate = null;
        try {
            startDate = formater.stringToValue(jTxtStartDate.getText());
            endDate = formater.stringToValue(jTxtEndDate.getText());
        } catch (ParseException ex) {
            Logger.getLogger(ReportDialog.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        //String reportTxt = createReport(startDate, endDate);
         String reportTxt = createReportHtml(startDate, endDate);
        jTextPane.setText(reportTxt);
    }//GEN-LAST:event_jBtnUpdateActionPerformed

    
