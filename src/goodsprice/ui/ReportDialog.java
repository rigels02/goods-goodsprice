     public ReportDialog( List<Good> goods, javax.swing.JFrame parent, boolean modal) {
          super(parent, modal);
        
           this.parent = parent;
        initComponents();
        
        jLblValidation.setText("");
        this.goods = goods;
     
        Date startDate=null;
        //By default make Start Date 1Jan for current year, the End date current date.
        int yy = ReportUtils.getYearPart(new Date());
        startDate= new Date(yy, 0, 1);
      
        String reportTxt = null;
        //reportTxt = createReport(startDate, new Date());
        reportTxt = createReportHtml(startDate, new Date());
        //jTextPane.setContentType("text/html");
        jTextPane.setText(reportTxt);
    }

     private String createReport(Date startDate, Date endDate){
       Reporter reporter = new Reporter(goods);
                
        List<MonthYearReport> reports = reporter.getTimeReportForAllShops(startDate,endDate);
        StringFormater formater = new StringFormater();
        String reportTxt = formater.printFmtStr(reports);
        return reportTxt;
     }
     
     private String createReportHtml(Date startDate, Date endDate){
       Reporter reporter = new Reporter(goods);
                
        List<MonthYearReport> reports = reporter.getTimeReportForAllShops(startDate,endDate);
        HtmlFormater formater = new HtmlFormater();
        String reportTxt = formater.printFmtStr_1(reports);
        return reportTxt;
     }
     
